    'Đ': 'D',
  };

  /// Normalize chuỗi để tìm kiếm:
  /// - Chuyển về chữ thường
  /// - Bỏ dấu tiếng Việt
  /// - Trim và chuẩn hóa khoảng trắng
  static String normalizeForSearch(String input) {
    final String lower = input.toLowerCase().trim();
    final StringBuffer buffer = StringBuffer();
    for (final String char in lower.split('')) {
      buffer.write(_vietnameseMap[char] ?? char);
    }
    return buffer.toString().replaceAll(RegExp(r'\s+'), ' ');
  }

  /// Bỏ dấu tiếng Việt mà không lowercase
  static String removeDiacritics(String input) {
    final StringBuffer buffer = StringBuffer();
    for (final String char in input.split('')) {
      buffer.write(_vietnameseMap[char] ?? char);
    }
    return buffer.toString();
  }
}
